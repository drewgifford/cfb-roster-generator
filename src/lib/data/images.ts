export function getRandomSkinTone() {
	const skinTones = Object.keys(headUrls).map(Number);
	const randomSkinTone = skinTones[Math.floor(Math.random() * skinTones.length)];
	return randomSkinTone;
}

export function getRandomHeadIndex(skinTone: number) {
	const heads = headUrls[skinTone as keyof typeof headUrls];
	const randomHeadIndex = Math.floor(Math.random() * heads.length);
	return randomHeadIndex;
}

export function getHeadUrl(skinTone: number, headIndex: number) {
	const heads = headUrls[skinTone as keyof typeof headUrls];
	const headUrl = heads[headIndex];
	return headUrl;
}
